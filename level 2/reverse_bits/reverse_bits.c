unsigned char	reverse_bits(unsigned char octet)
{
	char	s;
	int		i;

	i = 8;
	s = 0;
	while ((--i) >= 0)
	{
		s = (s << 1) | (octet % 2);
		octet /= 2;
	}
	return (s);
}
